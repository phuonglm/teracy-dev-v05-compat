node['teracy-dev']['aliases'].each do |ali|
  act = :create
  act = ali['action'].to_sym unless ali['action'].nil? or ali['action'].strip().empty?
  magic_shell_alias ali['name'] do
      command ali['command']
      action act
  end
end
